        public async Task<string> TranslateAsync(string text, string sourceLang, string targetLang)
        {
            if (string.IsNullOrWhiteSpace(text))
                return text;

            // Нормализация языковых кодов
            sourceLang = NormalizeLangCode(sourceLang);
            targetLang = NormalizeLangCode(targetLang);

            _logger.LogInformation($"Перевод: {text} ({sourceLang} -> {targetLang})");

            // Проверяем поддерживаемые языки
            var supportedLangs = new[] { "en", "ru", "tt" };

            if (sourceLang == "auto")
            {
                sourceLang = DetectLanguage(text);
                _logger.LogInformation($"Определен язык: {sourceLang}");
            }

            if (!supportedLangs.Contains(sourceLang))
            {
                return $"{text} [неподдерживаемый исходный язык: {sourceLang}]";
            }

            if (!supportedLangs.Contains(targetLang))
            {
                return $"{text} [неподдерживаемый целевой язык: {targetLang}]";
            }

            try
            {
                // 0. Сначала проверяем память переводов
                var memoryResult = await _memoryService.FindInMemoryAsync(text, sourceLang, targetLang);
                if (memoryResult != null)
                {
                    _logger.LogInformation($"Найден в памяти переводов: {memoryResult}");
                    return memoryResult;
                }

                string translatedText = "";

                // 1. Пробуем локальный словарь
                string dictionaryKey = $"{sourceLang}-{targetLang}";
                if (_dictionaries.TryGetValue(dictionaryKey, out var dict))
                {
                    if (dict.TryGetValue(text.Trim(), out var translation))
                    {
                        _logger.LogInformation($"Найден перевод в словаре: {translation}");
                        translatedText = EnsureCyrillicTatar(translation);
                    }
                }

                // 2. Если не нашли в словаре, используем оптимизированную логику
                if (string.IsNullOrEmpty(translatedText))
                {
                    translatedText = await TranslateWithFallback(text, sourceLang, targetLang);
                }

                // 3. Сохраняем успешный перевод в память переводов
                if (!string.IsNullOrEmpty(translatedText) && translatedText != text)
                {
                    await _memoryService.AddToMemoryAsync(text, translatedText, sourceLang, targetLang);
                }

                return translatedText;
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, $"Ошибка перевода: {text}");
                return GetFallback(text, sourceLang, targetLang);
            }
        }


