        // Гарантируем, что татарский текст в кириллице
        private string EnsureCyrillicTatar(string text)
        {
            if (string.IsNullOrEmpty(text))
                return text;

            // Преобразуем латинскую татарскую кириллицу (если есть)
            var latinToCyrillic = new Dictionary<string, string>
            {
                ["yazılım"] = "Программалау",
                ["yazilim"] = "Программалау",
                ["programming"] = "Программалау",
                ["sen"] = "Син",
                ["sızıq"] = "Мышка",
                // Добавьте другие преобразования при необходимости
            };

            foreach (var replacement in latinToCyrillic)
            {
                if (text.Contains(replacement.Key, StringComparison.OrdinalIgnoreCase))
                {
                    text = Regex.Replace(text, replacement.Key, replacement.Value, RegexOptions.IgnoreCase);
                }
            }

            return text;
        }

        // Получить русский перевод
        private async Task<string> GetRussianTranslation(string text, string sourceLang)
        {
            try
            {
                // Пробуем словарь
                string dictKey = $"{sourceLang}-ru";
                if (_dictionaries.TryGetValue(dictKey, out var dict))
                {
                    if (dict.TryGetValue(text.Trim(), out var translation))
                    {
                        return translation;
                    }
                }

                // Пробуем API
                var russianResult = await CallMyMemoryAPI(text, sourceLang, "ru");
                if (!IsFailedTranslation(russianResult, text))
                {
                    return russianResult;
                }
            }
            catch
            {
                // Игнорируем ошибки
            }

            // Если всё не сработало, возвращаем оригинал
            return text;
        }

        // Проверка, является ли перевод неудачным
        private bool IsFailedTranslation(string translation, string original)
        {
            if (string.IsNullOrEmpty(translation))
                return true;

            if (translation.Contains("Not Translated"))
                return true;

            if (translation.Contains("MYMEMORY WARNING"))
                return true;

            if (translation == original)
                return true;

            if (translation.StartsWith("[") && translation.EndsWith("]"))
                return true;

            return false;
        }

        // Вызов MyMemory API
        private async Task<string> CallMyMemoryAPI(string text, string sourceLang, string targetLang)
        {
            try
            {
                string encodedText = Uri.EscapeDataString(text);
                string url = $"https://api.mymemory.translated.net/get?q={encodedText}&langpair={sourceLang}|{targetLang}";

                _logger.LogInformation($"Запрос к MyMemory: {url}");

                var response = await _httpClient.GetAsync(url);
                response.EnsureSuccessStatusCode();

                string json = await response.Content.ReadAsStringAsync();

                using var doc = JsonDocument.Parse(json);

                if (doc.RootElement.TryGetProperty("responseData", out var responseData) &&
                    responseData.TryGetProperty("translatedText", out var translatedText))
                {
                    string result = translatedText.GetString() ?? text;
                    result = CleanResult(result);

                    _logger.LogInformation($"Перевод MyMemory: {result}");
                    return result;
                }

                return text;
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Ошибка запроса к MyMemory");
                throw;
            }
        }

        // Fallback перевод
        private string GetFallback(string text, string sourceLang, string targetLang)
        {
            // Пробуем словарь
            string dictKey = $"{sourceLang}-{targetLang}";
            if (_dictionaries.TryGetValue(dictKey, out var dict))
            {
                if (dict.TryGetValue(text.Trim(), out var translation))
                {
                    return translation;
                }
            }

            // Для татарского используем русский как fallback
            if (targetLang == "tt")
            {
                string ruDictKey = $"{sourceLang}-ru";
                if (_dictionaries.TryGetValue(ruDictKey, out var ruDict))
                {
                    if (ruDict.TryGetValue(text.Trim(), out var ruTranslation))
                    {
                        return ruTranslation; // Просто русский перевод
                    }
                }
                return text; // Или оригинальный текст
            }

            // Для остальных языков
            return targetLang switch
            {
                "ru" => text,
                "en" => text,
                _ => text
            };
        }

        // Очистка результата
        private string CleanResult(string text)
        {
            if (string.IsNullOrEmpty(text)) return text;

            // Удаляем предупреждения MyMemory
            if (text.Contains("MYMEMORY WARNING:"))
            {
                text = text.Split("MYMEMORY WARNING:")[0].Trim();
            }

            if (text.Contains("Not Translated"))
            {
                text = text.Replace("Not Translated", "").Trim();
            }

            return text.Trim();
        }

        // Нормализация языковых кодов
        private string NormalizeLangCode(string langCode)
        {
            return langCode.ToLowerInvariant() switch
            {
                "auto" => "auto",
                "eng" or "english" => "en",
                "rus" or "russian" or "ру" or "рус" => "ru",
                "tat" or "tatar" or "tt" or "тат" or "татар" => "tt",
                _ => langCode
            };
        }

        // Определение языка
        private string DetectLanguage(string text)
        {
            if (string.IsNullOrEmpty(text)) return "en";

            // Простое определение языка
            int cyrillicCount = text.Count(c => c >= 'А' && c <= 'я');
            int latinCount = text.Count(c => (c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));

            // Татарские символы
            bool hasTatar = text.Contains("ә") || text.Contains("ө") || text.Contains("ү") ||
                           text.Contains("җ") || text.Contains("ң") || text.Contains("һ");

            if (hasTatar) return "tt";
            if (cyrillicCount > latinCount) return "ru";
            return "en";
        }


