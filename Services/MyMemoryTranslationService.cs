        // ========== НОВЫЙ ОПТИМИЗИРОВАННЫЙ МЕТОД ==========
        private async Task<string> TranslateWithFallback(string text, string sourceLang, string targetLang)
        {
            // Для НЕ татарского языка - обычный перевод
            if (targetLang != "tt")
            {
                return await CallMyMemoryAPI(text, sourceLang, targetLang);
            }

            // Логика для татарского языка:
            _logger.LogInformation($"Перевод на татарский с fallback: {text} ({sourceLang})");

            string tatarTranslation = string.Empty;

            // 1. Пробуем словарь
            string dictKey = $"{sourceLang}-tt";
            if (_dictionaries.TryGetValue(dictKey, out var dict))
            {
                if (dict.TryGetValue(text.Trim(), out tatarTranslation))
                {
                    return EnsureCyrillicTatar(tatarTranslation);
                }
            }

            // 2. Пробуем MyMemory API напрямую
            try
            {
                tatarTranslation = await CallMyMemoryAPI(text, sourceLang, "tt");
                tatarTranslation = EnsureCyrillicTatar(tatarTranslation);

                if (!IsFailedTranslation(tatarTranslation, text))
                {
                    return tatarTranslation;
                }
            }
            catch
            {
                // Игнорируем ошибки API
            }

            // 3. Fallback на русский (если татарский не найден)
            _logger.LogWarning($"Не найден перевод на татарский для: '{text}'. Использую русский перевод.");

            if (sourceLang == "ru")
            {
                // Если исходный уже русский, возвращаем его
                return text;
            }
            else
            {
                // Для других языков переводим на русский
                var russianTranslation = await GetRussianTranslation(text, sourceLang);
                return russianTranslation; // Просто русский перевод, без пометок
            }
        }
